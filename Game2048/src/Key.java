public class Key {
    private int i;
    private int j;

    public Key(int i,int j){
        this.i = i;
        this.j = j;
    }

    public int geti(){

        return i;
    }
    public void seti(int i){

        this.i = i;
    }

    public int getj(){

        return j;
    }
    public void setj(int j){
        this.j = j;
    }
}
